function Button({ svg, text, open, setOpen }) {
  const [textColor, setTextColor] = useState("text-black");
  return (
    <div
      className={`p-3 pl-2 gap-4 flex font-bold ${
        text != "Main menu"
          ? `${textColor} cursor-pointer hover:bg-blue-200 duration-200 rounded-lg`
          : "text-2xl font-extrabold "
      }`}
      onClick={() => {
        setTextColor("text-blue-600");
      }}
    >
      <div className={`${text == "Main menu" ? "pt-1 " : ""}`}>{svg}</div>
      <div className={`shrink-0 duration-200 ${!open && "scale-0"}`}>
        {text}
      </div>
    </div>
  );
}
